/**
 * <h1>访问性</h1>
 *
 * <p>用于权限服务的返回值</p>
 *
 * @author Chuanwise
 */
public enum Accessibility {
    
    /**
     * 可访问
     */
    ACCESSIBLE,
    
    /**
     * 未定义
     */
    UNDEFINED,
    
    /**
     * 不可访问
     */
    INACCESSIBLE,
}
